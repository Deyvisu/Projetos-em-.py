valorA = int(input("Digite o valor de A: "))
valorB = int(input("Digite o valor de B: "))
valorC = int(input("Digite o valor de C: "))

if(valorA + valorB) < valorC:
    print("O valor da soma de A e B é menor que o valor de C")

elif(valorA + valorB) == valorC:
    print("O valor da soma de A e B é igual ao valor de C")

else:
    print("o valor da soma de A e B é maior que o valor de C")