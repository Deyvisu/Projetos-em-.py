#Escrever  um programa python que calcule a largura(length) de uma string.

texto = input("Escreva uma palavra: ")

print("A quantidade de letras dessa palavra é: ",len(texto))

print("A primeira letra da sua palavra é :",texto[0])

print("A última letra da sua palavra é: ",texto[-1])

print("A palavra invertida é: ",texto[::-1])